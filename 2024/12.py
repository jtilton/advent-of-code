directions = [(0, 1), (1, 0), (-1, 0), (0, -1)]
vertical = [(1, 0), (-1, 0)]
horizontal = [(0, 1), (0, -1)]

class TodayRunner(Runner):
    def parse(self, input):
        self.map = defaultdict(lambda: '')
        self.height = len(input)
        self.width = len(input[0])
        self.types = defaultdict(lambda: set())
        for i in range(len(input)):
            for j in range(len(input[0])):
                val = input[i][j]
                self.map[(i, j)] = val
                self.types[val].add((i, j))
    
    def perimeter(self, region):
        perimeter = 0
        for pt in region:
            ns = set([(pt[0]+d[0], pt[1]+d[1]) for d in directions])
            open = len(ns.difference(region))
            perimeter += open
        return perimeter
    
    def side_count(self, region):
        h_side_starts = defaultdict(lambda: set())
        v_side_starts = defaultdict(lambda: set())
        for pt in region:
            # right
            if (pt[0], pt[1]+1) not in region:
                v_side_starts[('r', pt[1])].add(pt[0])
            # left
            if (pt[0], pt[1]-1) not in region:
                v_side_starts[('l', pt[1] - 1)].add(pt[0])
            # down
            if (pt[0] + 1, pt[1]) not in region:
                h_side_starts[('d', pt[0])].add(pt[1])
            # up
            if (pt[0] - 1, pt[1]) not in region:
                h_side_starts[('u', pt[0] - 1)].add(pt[1])

        sides = 0
        for (d, axis), starts in v_side_starts.items():
            sorted_starts = sorted(list(starts))
            segments = 1
            for i in range(len(sorted_starts) - 1):
                if sorted_starts[i+1] != sorted_starts[i]+1:
                    segments += 1
            sides += segments

        for (d, axis), starts in h_side_starts.items():
            sorted_starts = sorted(list(starts))
            segments = 1
            for i in range(len(sorted_starts) - 1):
                if sorted_starts[i+1] != sorted_starts[i]+1:
                    segments += 1
            sides += segments
        return sides


    def find_region(self, spots):
        prev_ct = 0
        region = set()
        start = list(spots)[0]
        queue = [start]
        
        while len(spots) != prev_ct and len(queue):
            curr = queue.pop(0)
            prev_ct = len(spots)
            spots.discard(curr)
            region.add(curr)

            ns = [(curr[0]+d[0], curr[1]+d[1]) for d in directions]
            queue += [n for n in ns if (n in spots and not n in queue)]
        return (spots, region)

    def get_regions(self):
        regions = defaultdict(lambda: [])
        for type, spots in self.types.items():
            if True:
                remaining = spots.copy()
                while len(remaining):
                    (remaining, region) = self.find_region(remaining)
                    regions[type].append(region)
        return regions

    def part_a(self):
        regions = self.get_regions()
        return sum([sum([self.perimeter(region) * len(region) for region in rs]) for rs in regions.values()])
                

    def part_b(self):
        regions = self.get_regions()

        # for type, rs in regions.items():
        #     [print(type, self.side_count(r)) for r in rs]
        return sum([sum([self.side_count(region) * len(region) for region in rs]) for rs in regions.values()])


